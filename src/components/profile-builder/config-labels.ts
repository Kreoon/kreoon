/**
 * Obtiene la etiqueta traducida para una clave de configuracion
 * Si no existe traduccion, genera un fallback legible
 */
export function getConfigLabel(key: string): string {
  if (CONFIG_LABELS[key]) {
    return CONFIG_LABELS[key];
  }

  // Fallback: convertir camelCase a palabras con primera mayuscula
  const fallback = key
    .replace(/([A-Z])/g, ' $1')
    .replace(/^./, (str) => str.toUpperCase())
    .trim();

  // Warning en desarrollo para agregar traducciones faltantes
  if (import.meta.env.DEV) {
    console.warn(`[config-labels] Falta traduccion para: "${key}" -> usando: "${fallback}"`);
  }

  return fallback;
}

